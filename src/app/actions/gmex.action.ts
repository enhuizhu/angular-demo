export const GMEX_GET_STATUS = 'GMEX_GET_STATUS';
export const GMEX_RECIEVE_STATUS = 'GMEX_RECIEVE_STATUS';
export const GMEX_DATE_RANGE_CHANGE = 'GMEX_DATE_RANGE_CHANGE';
export const GMEX_LOADED_CHANGE = 'GMEX_LOADED_CHANGE';
export const GMEX_REFRESH_DATA = 'GMEX_REFRESH_DATA';

export const getGmexStatus = () => ({
  type: GMEX_GET_STATUS
});

export const refreshGmexData = () => ({
  type: GMEX_REFRESH_DATA
});

export const reciveGmexStatus = (payload) => ({
  type: GMEX_RECIEVE_STATUS,
  payload
});

export const changeGmexDateRange = (payload) => ({
  type: GMEX_DATE_RANGE_CHANGE,
  payload
});

export const changeGmexLoaded = (payload) => ({
  type: GMEX_LOADED_CHANGE,
  payload
});
